func NewBuzhash(window int) *Buzhash {
	return &Buzhash{
		window: make([]byte, window),
		size:   window,
		table:  randomTable,
	}
}

func rotl(v uint64, n int) uint64 {
	n &= 63
	return bits.RotateLeft64(v, n)
}

func (b *Buzhash) Roll(next byte) {
	if b.filled < b.size {
		b.hash = rotl(b.hash, 1)
		b.hash ^= b.table[next]

		b.window[b.pos] = next
		b.pos = (b.pos + 1) % b.size
		b.filled++
	}

	old := b.window[b.pos]

	b.hash = rotl(b.hash, 1)
	b.hash ^= rotl(b.table[old], b.size)
	b.hash ^= b.table[next]

	b.window[b.pos] = next
	b.pos = (b.pos + 1) % b.size
}

func (b *Buzhash) Sum64() uint64 {
	return b.hash
}

func (b *Buzhash) Ready() bool {
	return b.filled == b.size
}
